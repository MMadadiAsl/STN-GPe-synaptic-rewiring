
//***************************************************************************
//***************************************************************************

	#include <iostream>
	#include <cmath>
	#include <fstream>

	using namespace std;

//***************************************************************************
//***************************************************************************

int main(){

	const float PI = 4. * atan(1.);
	int n;
	float t, tn, amp_stim, I_stim, delta_stim, freq_stim;

	amp_stim = 200.0;
	freq_stim = 15.0;
	delta_stim = 5.0;

	ofstream outfile1("output1.txt");

//***************************************************************************
//***************************************************************************

	for (t = 0.0; t <= 1000.0; t = t + 0.01){
		tn = (1000.0 * n) / freq_stim;	
		if (tn <= t && t < (tn + delta_stim)) I_stim = amp_stim;
		else if ((tn + delta_stim) <= t && t < (tn + (2.0 * delta_stim))) I_stim = - amp_stim;
		else I_stim = 0.0; 
		outfile1 << t << '\t' << I_stim << endl;
		if (t > tn + (2.0 * delta_stim)) n++;
	}

//***************************************************************************
//***************************************************************************

	outfile1.close();

return 0;
}

