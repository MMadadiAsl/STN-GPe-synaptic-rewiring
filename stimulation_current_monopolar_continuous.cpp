	freq_stim = 50.0;
	delta_stim = 5.0;

	ofstream outfile1("output1.txt");

//***************************************************************************
//***************************************************************************

	for (t = 0.0; t <= 100.0; t = t + 0.01){
		I_stim = amp_stim * Step_Function(sin(2.0 * PI * t * (freq_stim * 0.001))) * (1.0 - Step_Function(sin(2.0 * PI * (t + delta_stim) * (freq_stim * 0.001))));
		outfile1 << t << '\t' << I_stim << endl;

	}

//***************************************************************************
//***************************************************************************

	outfile1.close();

return 0;
